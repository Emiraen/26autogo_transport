/* 简化：仅 0x20..0x5A 有字模；之外字符显示为空格 */

static void OLED_WriteCmd(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };  /* Co=0, D/C=0 */
    HAL_I2C_Master_Transmit(s_hi2c, s_oled_addr, buf, 2, 20);
}

static void OLED_WriteData(const uint8_t *data, uint16_t len)
{
    /* 控制字 0x40 + 数据 */
    uint8_t buf[129];
    buf[0] = 0x40;
    if (len > 128) len = 128;
    memcpy(&buf[1], data, len);
    HAL_I2C_Master_Transmit(s_hi2c, s_oled_addr, buf, len + 1, 50);
}

static void OLED_SetPos(uint8_t page, uint8_t col)
{
    OLED_WriteCmd(0xB0 + page);
    OLED_WriteCmd(0x00 + (col & 0x0F));
    OLED_WriteCmd(0x10 + ((col >> 4) & 0x0F));
}

uint8_t OLED_Probe(void)
{
    /* 依次探测 0x78 / 0x7A, 哪个 ACK 用哪个; 都不在返回 0 */
    static const uint8_t cand[] = { 0x78U, 0x7AU };
    if (s_hi2c == NULL) return 0;
    for (uint8_t i = 0; i < sizeof(cand); i++)
    {
        if (HAL_I2C_IsDeviceReady(s_hi2c, cand[i], 3, 20) == HAL_OK)
        {
            s_oled_addr = cand[i];
            return cand[i];
        }
    }
    return 0;
}

uint8_t OLED_GetAddr(void)
{
    return s_oled_addr;
}

int OLED_TryWrite(uint8_t write_addr)
{
    /* 绕过 IsDeviceReady, 直接向指定地址写一个命令字节 (0xAE display off)
     * 返回 HAL_StatusTypeDef 原值: 0=OK(设备ACK), 1=ERROR(NACK), 3=TIMEOUT */
    if (s_hi2c == NULL) return -1;
    uint8_t buf[2] = { 0x00, 0xAE };
    return (int)HAL_I2C_Master_Transmit(s_hi2c, write_addr, buf, 2, 20);
}

static int OLED_WriteCmdRetry(uint8_t cmd)
{
    /* 带重试的命令写, 返回 0=成功 */
    uint8_t buf[2] = { 0x00, cmd };
    for (int t = 0; t < 3; t++)
    {
        if (HAL_I2C_Master_Transmit(s_hi2c, s_oled_addr, buf, 2, 20) == HAL_OK)
            return 0;
        HAL_Delay(1);
    }
    return 1;
}

void OLED_Init(I2C_HandleTypeDef *hi2c)
{
    s_hi2c = hi2c;
    HAL_Delay(200);   /* 面板上电稳定 (部分模块需 >100ms) */
    /* 地址自适应: 探不到也以默认 0x78 继续尝试初始化 */
    (void)OLED_Probe();
    static const uint8_t init_cmds[] = {
        0xAE,        /* display off */
        0xD5, 0x80,  /* clock div */
        0xA8, 0x3F,  /* multiplex 64 */
        0xD3, 0x00,  /* offset 0 */
        0x40,        /* start line 0 */
        0x8D, 0x14,  /* charge pump on (内部升压, 4 针模块必须) */
        0x20, 0x02,  /* Page Addressing Mode */
        0xA1,        /* seg remap */
        0xC8,        /* COM scan dec */
        0xDA, 0x12,  /* COM pins */
        0x81, 0xFF,  /* contrast 最大 */
        0xD9, 0xF1,  /* precharge */
        0xDB, 0x40,  /* vcomh */
        0xA4,        /* RAM follow */
        0xA6,        /* normal display */
        0xAF         /* display on */
    };
    int fail = 0;
    for (uint8_t i = 0; i < sizeof(init_cmds); i++)
        fail += OLED_WriteCmdRetry(init_cmds[i]);
    s_oled_init_fail = fail;
    HAL_Delay(50);
    OLED_Clear();
}

int OLED_GetInitFail(void)
{
    return s_oled_init_fail;
}

void OLED_Clear(void)
{
    uint8_t zeros[128] = {0};
    for (uint8_t p = 0; p < 8; p++)
    {
        OLED_SetPos(p, 0);
        OLED_WriteData(zeros, 128);
    }
}

void OLED_FillTest(uint8_t pattern)
{
    /* 全屏填充 pattern (0xFF=全亮, 0x00=全灭), 用于点屏硬件测试 */
    uint8_t row[128];
    memset(row, pattern, sizeof(row));
    for (uint8_t p = 0; p < 8; p++)
    {
        OLED_SetPos(p, 0);
        OLED_WriteData(row, 128);
    }
}

static void OLED_DrawChar8x16(uint8_t page, uint8_t col, char c)
{
    uint8_t idx;
    if ((uint8_t)c >= 0x20 && (uint8_t)c <= 0x5A)
        idx = (uint8_t)c - 0x20;
    else if (c >= 'a' && c <= 'z')
        idx = (uint8_t)(c - 'a' + 'A') - 0x20;  /* 小写映射成大写 */
    else
        idx = 0;  /* 空格 */

    /* 上半 */
    OLED_SetPos(page, col);
    OLED_WriteData(&F8x16[idx][0], 8);
    /* 下半 */
    OLED_SetPos(page + 1, col);
    OLED_WriteData(&F8x16[idx][8], 8);
}

void OLED_ShowString(uint8_t page, uint8_t col, const char *str)
{
    while (*str)
    {
        if (col > 120) break;
        OLED_DrawChar8x16(page, col, *str);
        col += 8;
        str++;
    }
}
